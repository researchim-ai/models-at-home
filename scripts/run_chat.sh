#!/bin/bash
# =============================================================================
# HomeLLM - Скрипт для чата с обученной моделью
# =============================================================================
# Использование:
#   ./scripts/run_chat.sh                           # Интерактивный режим
#   ./scripts/run_chat.sh --prompt "Привет!"        # Одноразовый запрос
# =============================================================================

set -e

cd "$(dirname "$0")/.."

MODEL_DIR="${MODEL_DIR:-out/home_pretrain/final_model}"
DEVICE="${DEVICE:-cuda}"

echo "=============================================="
echo "  HomeLLM Chat"
echo "=============================================="
echo "Модель: $MODEL_DIR"
echo "Device: $DEVICE"
echo "=============================================="

python -m homellm.cli.chat \
    --model_dir "$MODEL_DIR" \
    --device "$DEVICE" \
    "$@"

